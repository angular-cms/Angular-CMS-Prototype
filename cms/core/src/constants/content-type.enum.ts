
/**
 * The enum to define the type of content includes `page`, `block`, `media`, `page_partial`, `folder_block`, `folder_media`
 */
export enum ContentTypeEnum {
    /**
     * The page type
     */
    Page = 'page',
    /**
     * The page partial
     */
    PagePartial = 'page_partial',
    Block = 'block',
    Media = 'media',
    FolderBlock = 'folder_block',
    FolderMedia = 'folder_media'
}
